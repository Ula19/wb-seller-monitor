def tge(key: str) -> str:
    """Возвращает <tg-emoji> тег для вставки в HTML-сообщения."""
    eid, fb = _EMOJI.get(key, ("", ""))
    if eid:
        return f'<tg-emoji emoji-id="{eid}">{fb}</tg-emoji>'
    return fb


def esc(s) -> str:
    """Экранирует HTML-спецсимволы в пользовательских строках."""
    return html.escape(str(s or ""))
